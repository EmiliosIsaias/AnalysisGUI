% hObject    handle to pushbutton5 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%read in user data
beforeT=str2double(get(handles.edit5,'String'));
afterT=str2double(get(handles.edit6,'String'));
% binsize gets 5 as default
binsize=str2double(get(handles.edit8,'String')); 


set(handles.edit14,'String',['-' get(handles.edit5,'String')]);
set(handles.edit16,'String',get(handles.edit6,'String'));
Conditions=newAnalysis_guihelper(handles.CONDITIONS,handles.TRIGGERS,handles.RAWRESPONSE,handles.SPIKES,...
    beforeT,afterT,binsize, handles.PPMS,handles.SAVEFILE,get(handles.checkbox5,'value'));
handles.CONDITIONS=Conditions;

set(handles.pushbutton2,'enable','on');
set(handles.pushbutton7,'enable','on');
guidata(hObject, handles);


% --- Executes on button press in pushbutton7. ---------- 3) Analyze bursts
function pushbutton7_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton7 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
eval(['useConditions=' get(handles.edit3,'String') ';']);
useConditions % PSTH maximums box:
delayAfterTrigger=str2double(get(handles.edit19,'String'))
windowEnd=str2double(get(handles.edit20,'String'))
zeroflag= get(handles.checkbox7,'Value')

[burstfig1 burstfig2]=BurstPlots(handles.CONDITIONS(useConditions),handles.PPMS, delayAfterTrigger,windowEnd,...
        str2double(get(handles.edit19,'String')), str2double(get(handles.edit20,'String')));
[get(handles.edit24,'String'), 'Burst1']
set(burstfig1,'name',[get(handles.edit24,'String'), 'Burst1']);
set(burstfig2,'name',[get(handles.edit24,'String'), 'Burst2']);
handles.burstfig1=burstfig1;
handles.burstfig2=burstfig2;
guidata(hObject, handles);



autosave=get(handles.checkbox8,'value');
if autosave
    saveas(burstfig1, get(burstfig1,'name'), 'fig');
    saveas(burstfig2, get(burstfig2,'name'), 'fig');
end




